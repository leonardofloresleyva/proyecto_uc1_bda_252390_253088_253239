    private void guardarDatos() throws PresentacionException{
        try {

            String nombres = Nombre.getText().trim();
            String apellidoPaterno = ApellidoPaterno.getText().trim();
            String apellidoMaterno = ApellidoMaterno.getText().trim();
            String telefono = Telefono.getText().trim();
            String colonia = Colonia.getText().trim();
            String calle = Calle.getText().trim();
            String numero = Numero.getText().trim();
            
            LocalDate fechaNacimiento = FechaNacimiento.getDate();

            PacienteViejoDTO paciente = new PacienteViejoDTO(
                    pacienteActualizado.getUsuario(), // Aseg√∫rate de tener el usuario
                    pacienteActualizado.getContrasenia(), // Tambi√©n la contrase√±a
                    nombres, apellidoPaterno, apellidoMaterno, telefono, fechaNacimiento, "Activo", colonia, calle, numero
            );

            PacienteBO pacienteBO = new PacienteBO(new Conexion());
            boolean actualizado = pacienteBO.actualizarDatosPaciente(paciente);

            if (actualizado) {
                pacienteActualizado.setNombres(nombres);
                pacienteActualizado.setApellidoPaterno(apellidoPaterno);
                pacienteActualizado.setApellidoMaterno(apellidoMaterno);
                pacienteActualizado.setTelefono(telefono);
                pacienteActualizado.setColonia(colonia);
                pacienteActualizado.setCalle(calle);
                pacienteActualizado.setNumero(numero);

                JOptionPane.showMessageDialog(this, "Datos actualizados correctamente.", "√âxito", JOptionPane.INFORMATION_MESSAGE);
                PerfilPaciente nuevaVentana = new PerfilPaciente(pacienteActualizado);
                nuevaVentana.setVisible(true);
                this.dispose();
            }
        } catch (NegocioException e) {
            throw new PresentacionException(e.getMessage(), e);
        }
    }
    
